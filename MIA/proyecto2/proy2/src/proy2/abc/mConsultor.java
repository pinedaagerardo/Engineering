    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String id=jTextField4.getText();
        con.abrir(user, pass);
        ResultSet rs=con.executeQ("select idconsultor from consultor where idconsultor="+id);
        try{
            if(!rs.next()){
                JOptionPane.showMessageDialog(this, "No existe el consultor");
                con.cerrar();
                return;
            }
        }catch(Exception e){}

        con.execute("delete from consultor where idconsultor="+id);
        abc.log("modificacion: delete from consultor where idconsultor="+id);
        jTextField4.setText("");
        con.cerrar();
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id=jTextField4.getText();
        con.abrir(user, pass);
        if(id.trim().length()==0){//insert
            String s="insert into consultor (area_idArea,nombres,email) values ("+jTextField1.getText()+",'"+jTextField2.getText()+"','"+jTextField3.getText()+"')";
            con.execute(s);
            abc.log("modificacion: "+s);
        }else{//update
            ResultSet rs=con.executeQ("select idconsultor from consultor where idconsultor="+id);
            try{
                if(!rs.next()){
                    JOptionPane.showMessageDialog(this, "No existe el consultor");
                    con.cerrar();
                    return;
                }
            }catch(Exception e){}

            String s="update consultor set area_idArea='"+jTextField1.getText()+"',nombres='"+jTextField2.getText()+"',email='"+jTextField3.getText()+"' where idconsultor="+id;
            con.execute(s);
            abc.log("modificacion: "+s);
        }
        con.cerrar();
    }//GEN-LAST:event_jButton1ActionPerformed

