    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String id=jTextField4.getText();
        con.abrir(user, pass);

        ResultSet rs=con.executeQ("select * from consultor where idconsultor="+id);
        try{
            if(rs.next()){
                jTextField1.setText(rs.getString("AREA_IDAREA"));
                jTextField2.setText(rs.getString("NOMBRES"));
                jTextField3.setText(rs.getString("EMAIL"));
            }else
                JOptionPane.showMessageDialog(this, "No existe el consultor");

        }catch(Exception e){}
        con.cerrar();
    }//GEN-LAST:event_jButton4ActionPerformed

